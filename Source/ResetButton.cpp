void ResetButton::paintButton( Graphics& g, bool isMouseOverButton, bool isButtonDown )
{
	if (isMouseOverButton)
	{
		juce::Rectangle<float> r(getLocalBounds().toFloat());
		r.reduce(1.0f, 1.0f);
		g.setColour(Colours::lightsteelblue.withAlpha(0.5f));
		g.fillRoundedRectangle( r, 3.0f);
	}

	{
		juce::Rectangle<float> r(getLocalBounds().toFloat());

		float shrink = (isButtonDown) ? 5.0f : 4.0f;
		r.reduce(shrink, shrink);

		AffineTransform transform(AffineTransform::verticalFlip((float)getHeight()));
		transform = transform.rotated(float_Pi, getWidth() * 0.5f, getHeight() * 0.5f);
		g.addTransform(transform);

		sharedSVG->svg->drawWithin(g, r, RectanglePlacement::centred, 1.0f);
	}
}